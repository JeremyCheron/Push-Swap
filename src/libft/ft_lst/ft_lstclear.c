#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		temp = (*lst)->next;
		printf("Deleting node: %p\n", *lst);
		del((*lst)->content);
		free(*lst);
		*lst = temp;
	}
	*lst = NULL;
}
